package offer;

/**
 * 输入一棵二叉树，求该树的深度。
 * 从根结点到叶结点依次经过的结点（含根、叶结点）形成树的一条路径，
 * 最长路径的长度为树的深度。
 * @author zhouliang
 *
 */
class test39 {
    public int TreeDepth(TreeNode root) {
    	if(root==null){
    		return 0;
    	}
    	int left = TreeDepth(root.left)+1;
    	int right = TreeDepth(root.right)+1;
        return left>right? (left+1):(right+1);
    }
}
